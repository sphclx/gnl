#include "libft.h"

size_t	ft_strlcat(char *dst, const char *src, size_t size)
{
	size_t	i;
	size_t	dest;
	size_t	srclen;

	i = 0;
	dest = ft_strlen(dst);
	srclen = ft_strlen(src);
	if (size <= dest)
		return (srclen + size);
	while ((dst[i] != '\0') && i < (size - 1))
		i++;
	while (*src && i < (size - 1))
	{
		dst[i] = *src;
		i++;
		src++;
	}
	dst[i] = '\0';
	return (dest + srclen);
}
