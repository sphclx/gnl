int			ft_atoi(char *str)
{
	int		sign;
	long	val;

	sign = 1;
	val = 0;
	while (*str == '\n' || *str == '\v' || *str == '\f' ||
					*str == ' ' || *str == '\t' || *str == '\r')
		str++;
	if (*str == '-')
		sign = -1;
	if (*str == '+' || *str == '-')
		++str;
	while ((*str >= '0') && (*str <= '9'))
	{
		val = (val * 10) + (*str - '0');
		str++;
	}
	return (val * sign);
}
